<!-- vue3부터는 filter가 제거 되었다. vue2 에서 테스트 -->
<template>
  <div>
    <label class="styled-input" id="lblText">{{ receiveText | capitalize}} </label>
    <br>

    <input class="styled-input" v-model="receiveText" />

  </div>
</template>

<script>
import Vue from 'vue'

//필터정의
Vue.filter('capitalize', function(value) {
  if (!value) return '';
  value = value.toString();
  return value.charAt(0).toUpperCase() + value.slice(1);
})

export default {
  data () {
    return {
      receiveText: ''
    }

  },


}
</script>

